def vgg16_bn(pretrained = False, model_root=None, cfg= None, **kwargs):
    model_urls = 'https://download.pytorch.org/models/vgg16_bn-6c64b313.pth'
    if cfg == None:
        cfg = [64, 64, 'M', 128, 128, 'M', 256, 256, 256, 'M', 512, 512, 512, 'M', 512, 512, 512, 'M']
        model = VGG(make_layers(cfg, batch_norm=True), cfg= cfg[:-1], **kwargs)
    else:
        model = VGG(make_layers(cfg, batch_norm=True), cfg= cfg, **kwargs)

    if pretrained:
        model.load_state_dict(model_zoo.load_url(model_urls, model_root))

    return model

